public class Print3 {
    public static void main(String[] args) {
        System.out.println(17 + 35);
        System.out.println("17 + 35");

        // 자바에서는 문자열을 표시할 때는 반드시 ""를 사용하여야 한다.
        System.out.println("17 + 35 = " + 17+35);
        System.out.println("17 + 35 = " +(17+35));
    }
}
